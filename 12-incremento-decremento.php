<?php

$valor = 10;

// Pré-Incremento
echo ++$valor; // Incrementa antes e depois mostra o valor
echo "<br/>";

$valor = 20;
// Pos-Incremento
echo $valor++; // Mostra o valor da viariavel primeiro depois incrementa
echo "<br/>";

$valor = 30;
// Pré-Decremento
echo --$valor; // Decrementa antes e depois mostra o valor
echo "<br/>";

$valor = 40;
// Pos-Decremento
echo $valor--; // Mostra o valor da viariavel primeiro depois decrementa